export function actorTransform(profile) {
  const transform = profile?.transform || {};
  return {
    posX: transform.posX ?? 0,
    posY: transform.posY ?? 0,
    posZ: transform.posZ ?? 0,
    x: transform.rotationX ?? profile?.defaultRotX ?? 0,
    y: transform.rotationY ?? 0,
    z: transform.rotationZ ?? 0,
    scale: transform.scale ?? 100,
    basisX: transform.basisX ?? 0,
    basisY: transform.basisY ?? 0,
    basisZ: transform.basisZ ?? 0,
  };
}

export function clipOptions(profile, fallback = {}) {
  return { ...fallback, ...(profile?.ownClipOptions || {}) };
}
